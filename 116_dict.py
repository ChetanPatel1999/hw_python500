# Dictionary items are ordered, changeable, and does not allow duplicates. 
# Dictionary items are presented in key:value pairs, and can be referred to by 
# using the key name. 

num_factorial={1:1,2:2,3:6,4:24,5:120}
print(num_factorial)
num_factors={12:[1,2,3,4,6,12],15:[1,3,5,15],10:[1,2,5,10]}
print(num_factors)
marks={"hindi":50,"math":80}
print(marks)
stationary={"pen":5,"book":50}
print(stationary)
rno=101
name="chetan"
student={rno:name}
print(student)